package com.tabulaw.cassandra.om.factory.exception;


public class HelenaException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public HelenaException() {
		super();
	}

	public HelenaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public HelenaException(String arg0) {
		super(arg0);
	}

	public HelenaException(Throwable arg0) {
		super(arg0);
	}	
}
